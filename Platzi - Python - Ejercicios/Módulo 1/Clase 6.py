#Operadores numéricos
a = 10
b = 3
print("Suma: ", a + b)
print("Resta: ", a - b)
print("Multiplicación: ", a * b)
print("Potenciación: ", a ** b)
print("Divisón: ", a / b)
print("Parte entera de la División: ", a // b)
#Módulo es igual al resto de una división
print("Módulo: ", a % b)
a = a + 2
print(a)
a += 2
print(a)
a /= 2
print(a)
